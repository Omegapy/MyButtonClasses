//------------------------------------------------------------------------------------
// Program main entry point
//------------------------------------------------------------------------------------
int main()
{
    //--------------------------------------------------------------------------------------

    // variables
    //--------------------------------------------------------------------------------------
    const int screenWidth = 800;
    const int screenHeight = 450;

    //--------------------------------------------------------------------------------------

    // Initialization Window, OpenGL context, Texture Loads need to be call after this point 
    //--------------------------------------------------------------------------------------
    // 
    // Enable config flags for resizable window and vertical synchro
    SetConfigFlags(FLAG_WINDOW_RESIZABLE | FLAG_VSYNC_HINT);
    InitWindow(screenWidth, screenHeight, "My Button Classes");


    SetTargetFPS(60);               // Set our game to run at 60 frames-per-second

    //--------------------------------------------------------------------------------------

    // Initialize audio device and context
    //--------------------------------------------------------------------------------------
    InitAudioDevice();

    //--------------------------------------------------------------------------------------

    // 2D textures/ 3D textures / models
    //--------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------

    // Sounds
    //--------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------

    // Font
    //--------------------------------------------------------------------------------------
    // 
    Font font1 = LoadFont("resources/font/monofonto.otf");
    Font font2 = LoadFont("resources/font/segoeui.ttf");

                                     
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //---------------------------------------------- comment type-1 -------------------------------------------------
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    //----------------------------------------------------------------------------------------------------------------
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //---------------------------------------------------------------------------------------------------------------
    //                                               comment type-2 
    //--------------------------------------------------------------------------------------------------------------- 
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //---------------------------------------------- comment type-2 -------------------------------------------------
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ButtonR btn1;
    //btn1.mod_is_shadow(false);
    btn1.mod_button_color(PURPLE);
    btn1.mod_border_thickness(30);
    //btn1.mod_is_border(false);
    //btn1.mod_text("Continue");
    //btn1.mod_text_no_resize("up");
    //btn1.mod_font_size(24);
    //btn1.mod_font_size_no_resize(34);

    ButtonR btn2("Button", 100, 225, font1);
    ButtonR btn3("Button", 100, 300, font2);
    // Main game loop
    while (!WindowShouldClose())    // Detect window close button or ESC key
    {
        //--------------------------------------------------------------------------------------

        // Update
        //-------------------------------------------------------------------------------------- 
        btn1.update();
        btn2.update();
        btn3.update();
        


                                        //////////////////////////////////////////////////
                                       /*-----------------------------------------------
                                        |                                              |
                                        |           Game Instance Rendering            |
                                        |                                              |
                                        -----------------------------------------------*/
                                        //////////////////////////////////////////////////
                                         
        //----------------------------------------------------------------------------------

        // Draw
        //----------------------------------------------------------------------------------

        BeginDrawing();

            ClearBackground(WHITE);

            btn1.draw();
            btn2.draw();
            btn3.draw();

        EndDrawing();

        //----------------------------------------------------------------------------------
    }

    //--------------------------------------------------------------------------------------

    // De-Initialization
    //--------------------------------------------------------------------------------------
    
    
    CloseWindow();        // Close window and OpenGL context

    //--------------------------------------------------------------------------------------

    return 0;
}